const int N = 2e5 + 5;

void solve () {
  int32_t t; cin >> t;
  while (t--) {
    int32_t n; cin >> n;
    int32_t ans = 0;
    for (int32_t x = 1; x * x <= n; x++) {
      if (n % x == 0) ans += ((n / x != x) ? 2 : 1);
    }
    cout << ans << endl;
  }
}

