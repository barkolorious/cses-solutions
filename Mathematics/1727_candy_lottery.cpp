const int N = 2e5 + 5;

double pwr (double x, int y) {
  double res = 1;
  while (y) {
    if (y & 1) res = res * x;
    x = x * x;
    y >>= 1; 
  }
  return res;
}

void solve () {
  int n, k; cin >> n >> k;
  double ans = 0;
  for (double i = 1; i <= k; i++) {
    ans += i * (pwr(i / k, n) - pwr((i - 1) / k, n));
  }
  cout << fixed << setprecision(6) << ans << endl;
}

