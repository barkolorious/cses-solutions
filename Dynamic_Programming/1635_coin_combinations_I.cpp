const int N = 2e5 + 5;
const int mod = 1e9 + 7;

void solve () {
  int n, x; cin >> n >> x;
  int c[n], dp[x + 1]{};
  for (int& coin : c) cin >> coin;
  dp[0] = 1;
  for (int i = 0; i <= x; i++) {
    for (int coin : c) {
      if (i + coin > x) continue;
      dp[i + coin] = (dp[i + coin] + dp[i]) % mod;
    }
  }
  cout << dp[x] << endl;
}

